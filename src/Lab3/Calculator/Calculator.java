package Lab3.Calculator;

public class Calculator {
    public int sum(int nr1, int nr2) {
        int sum = nr1 + nr2;
        return sum;
    }

    public float divide(float nr1, float nr2) {
        float division = nr1 / nr2;
        return division;
    }

    public int calculateDifference(int nr1, int nr2) {
        int difference = nr1 - nr2;
        return difference;

    }

    public int multiply(int nr1, int nr2) {
        int multuply = nr1 * nr2;
        return multuply;
    }
}
